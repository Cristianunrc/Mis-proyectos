package simularHerenciaMultiple;

/**
 * Interface que define el perfil de metodos suma y resta.
 */
public interface Operaciones1 {
	
	/**
	 * @param x un entero
	 * @param y un entero
	 * @return la suma de ambos
	 */
	public int suma (int x, int y);
	
	/**
	 * @param x un entero
	 * @param y un entero
	 * @return la resta de ambos
	 */
	public int resta (int x, int y);

}
